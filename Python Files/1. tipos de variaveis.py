nome = "Thiago" # Isto é uma string (texto)
idade = 17 # Isto é um int (inteiro)
altura = 1.75 # Isto é um float (nº de ponto flutuante). Pode ser interpretado como double (nº real) tbm

#podemos fazer de duas formas

# 1º forma
print('Meu nome é ', nome,'.')
print('Minha idade é ', idade,'.')
print('Minha altura é ', altura,'.')

# Enfeitando o código ;)
print('\n----------------------------\n')

# 2º forma
print(f"Meu nome é {nome}.")
print(f"Minha idade é {idade}.")
print(f"Minha altura é {altura}.")

